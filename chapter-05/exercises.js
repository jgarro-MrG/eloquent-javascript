
// /////////////////////////////////////////////////////////////////////////////
// flatten /////////////////////////////////////////////////////////////////////
// /////////////////////////////////////////////////////////////////////////////

function flatten(higherArray) {
  let flattenedArray = [];
  higherArray.forEach((array) => flattenedArray = flattenedArray.concat(array))
  return flattenedArray;
}

// /////////////////////////////////////////////////////////////////////////////
// loop ////////////////////////////////////////////////////////////////////////
// /////////////////////////////////////////////////////////////////////////////

function loop(value, test, update, todo) {
  while (test(value)) {
     todo(value)
     value = update(value)
  }  
}

// /////////////////////////////////////////////////////////////////////////////
// every ///////////////////////////////////////////////////////////////////////
// /////////////////////////////////////////////////////////////////////////////

function every(array, test) {
  let result = true;
  array.forEach((element) => { result = result && test(element)})
  return result;
}

function everyAlt(array, test) {
  return !array.some((element) => !test(element));
}

// /////////////////////////////////////////////////////////////////////////////
// dominantDirection ///////////////////////////////////////////////////////////
// /////////////////////////////////////////////////////////////////////////////

function dominantDirection(text) {
  let ltrCount, rtlCount = 0;
  for (let i = 0; i < text.length; i++) {
    const script = characterScript(text.charCodeAt(i));
    if (script) {
      if (script.direction === 'ltr') ltrCount++;
      if (script.direction === 'rtl') rtlCount++;
    }
  }
  return ltrCount > rtlCount ? 'ltr' : 'rtl';
}

